Byte * PerspectiveDataToCache(Byte *data) {
    int possible = data[0];
    int central = data[1];
    Byte bondScreen = data[2];
    int putIn = data[3];
    if (!possible) return data + putIn;
    for (int i = putIn; i < putIn + central; i++) {
        int value = data[i] - bondScreen;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[putIn + central] = 0;
    return data + putIn;
}

NSString *StringFromPerspectiveData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PerspectiveDataToCache(data)];
